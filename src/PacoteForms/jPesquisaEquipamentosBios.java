    public ArrayList<ControlEquiUnimed> listBiosUnimed(String ValToSearch) {
        ArrayList<ControlEquiUnimed> CadastroList = new ArrayList<>();

        Statement st;
        ResultSet rs;

        try {
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM controleequipamentounimed WHERE Concat (CodEquip, Patrimonio, Equipamento, Modelo, Departamento, DataEnvio, DataRetorno, Selecione, orcamento, CentroCusto) LIKE ('%" + ValToSearch + "%')";
            rs = st.executeQuery(searchQuery);

            ControlEquiUnimed pesquisaBios;

            while (rs.next()) {
                pesquisaBios = new ControlEquiUnimed(
                        rs.getInt("CodEquip"),
                        rs.getInt("Patrimonio"),
                        rs.getString("Equipamento"),
                        rs.getString("Modelo"),
                        rs.getString("Departamento"),
                        rs.getString("DataEnvio"),
                        rs.getString("DataRetorno"),
                        rs.getString("Selecione"),
                        rs.getFloat("orcamento"),
                        rs.getInt("CentroCusto"));
                CadastroList.add(pesquisaBios);
            }

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

        return CadastroList;
    }

    public void findControlEquiUnimed() {
        ArrayList<ControlEquiUnimed> listcontrolBios = listBiosUnimed(jTxtMostraUnimed.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Codigo", "Patrimonio", "Equipamento", "Modelo", "Departamento", "Data Envio", "Data Retorno", "Status", "Orcamento", "Centro de Custo"});
        Object[] row = new Object[10];

        for (int i = 0; i < listcontrolBios.size(); i++) {

            row[0] = listcontrolBios.get(i).getCodEquip();
            row[1] = listcontrolBios.get(i).getPatrimonio();
            row[2] = listcontrolBios.get(i).getEquipamento();
            row[3] = listcontrolBios.get(i).getModelo();
            row[4] = listcontrolBios.get(i).getDepartamento();
            row[5] = listcontrolBios.get(i).getDataEnvio();
            row[6] = listcontrolBios.get(i).getDataRetorno();
            row[7] = listcontrolBios.get(i).getSelecione();
            row[8] = listcontrolBios.get(i).getOrcamento();
            row[9] = listcontrolBios.get(i).getCentroCusto();

            model.addRow(row);
        }
        jTblUnimed.setModel(model);

        //int somaLinhas = jTblUser.getRowCount();
        //jTxtContador.setText(Integer.toString(somaLinhas));
    }

